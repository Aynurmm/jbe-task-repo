package az.academy.turing;

public class Char {
    public static void main(String[] args) {
        char c='a';
        System.out.print(c+"\n");
        System.out.println(c);
        System.out.printf("%s\n" , c);

        boolean b=true;
        System.out.print(!b+"\n");
        System.out.println(b);
        System.out.printf("%s" , !b);
    }
}
